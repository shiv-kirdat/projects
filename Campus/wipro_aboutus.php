					 				<h3>Company Information</h3>
					 				<p> Wipro Limited is an Indian multinational corporation that provides information technology, consulting and business process services. It is headquartered in Bangalore, Karnataka, India.

In 2013, Wipro separated its non-IT businesses and formed the privately owned Wipro Enterprises.</p>
					 				<p><b>History</b></p>
<p><b>Early years</b></p><p>
The company was incorporated on 29 December 1945 in Amalner, Maharashtra by Mohamed Premji as "Western India Palm Refined Oil Limited", later abbreviated to "Wipro". It was initially set up as a manufacturer of vegetable and refined oils in Amalner, Maharashtra, British India, under the trade names of Kisan, Sunflower, and Camel.

In 1966, after Mohamed Premji's death, his son Azim Premji returned from Stanford University and took over Wipro as its chairman at the age of 21.[8][9]

During the 1970s and 1980s, the company shifted its focus to new opportunities in the IT and computing industry, which was at a nascent stage in India at the time. On 7 June 1977, the name of the company changed from Western India Vegetable Products Limited, to Wipro Products Limited. In 1982, the name was changed again, from Wipro Products Limited to Wipro Limited. Wipro continued to expand in the consumer products domain with the launch of "Ralak" a Tulsi based family soap and "Wipro Jasmine", a toilet soap. In 1988 Premji took a loan of 1 million dollars from Sonkar and Sons to set up the project. Later the loan was waived.
</p>
<p><b>1986–1992</b></p><p>
In 1988, Wipro added heavy-duty industrial cylinders and mobile hydraulic cylinders to its product line.A joint venture company with the United States' General Electric in the name of Wipro GE Medical Systems Pvt. Ltd. was set up in 1989 for the manufacture, sales, and service of diagnostic and imaging products. In 1991, tipping systems and Eaton hydraulic products were launched. The Wipro Fluid Power division, in 1992, developed the capability to offer standard hydraulic cylinders for construction equipment and truck tipping systems. The "Santoor" talcum powder and "Wipro Baby Soft" range of baby toiletries were launched in 1990.
</p>
<p><b>1994–2000</b></p><p>
In 1995, Wipro set up an overseas design centre, Odyssey 21, for the projects of overseas clients. Wipro Infotech and Wipro Systems were amalgamated with Wipro in April that year. Five of Wipro's manufacturing and development facilities secured the ISO 9001 certification during 1994–95.

In 1999, Wipro acquired Wipro Acer.Wipro released new products such as the Wipro SuperGenius personal computers (PCs). In 1999, the product was the one Indian PC range to obtain US-based National Software Testing Laboratory (NSTL) certification for the Year 2000 (Y2K) compliance in hardware for all models.

Wipro Limited joined with KPN (Royal Dutch telecom) to form a joint venture company "Wipro Net Limited" to provide internet services in India. In 2000 Wipro launched Wipro OSS Smart and Wipro WAP Smart. In the same year, Wipro was listed on the New York Stock Exchange.
</p>
<p><b>2001–2010</b></p><p>
In February 2002, Wipro became the first software technology and services company in India to be ISO 14001 certified. Wipro Consumer Care and Lighting Group entered the market of compact fluorescent lamps, with the launch of a range of CFL, under the brand name of Wipro Smartlite. As the company grew, a study revealed that Wipro was the fastest wealth creator for 5 years (1997–2002). It set up a wholly owned subsidiary company (Wipro Consumer Care Limited) to manufacture consumer care and lighting products.In 2004 Wipro joined the billion dollar club.It also partnered with Intel for i-shiksha. In 2006, Wipro acquired cMango Inc., a US-based technology infrastructure consulting firm, and a Europe-based retail provider.In 2007, Wipro signed a deal with Lockheed Martin. It also agreed to acquire Oki Techno Centre Singapore Pte Ltd (OTCS) and signed an R&D partnership contract with Nokia Siemens Networks in Germany.

In 2008, Wipro’s entered the clean energy business with Wipro Eco Energy.
</p>
<p><b>2011-2018</b></p><p>
In April 2011, Wipro signed an agreement with Science Applications International Corporation (SAIC) for the acquisition of their global oil and gas information technology practice.[34][35] In 2012, Wipro employed more than 70,000 H-1B visa professional temporary workers in the United States.

In 2012, Wipro acquired Australian Trade Promotions Management firm Promax Applications Group (PAG) for $35 million. Also, in that year, Wipro Ltd. demerged its consumer care, lighting, furniture, infrastructure engineering (hydraulics and water and medical diagnostic business) into a separate company to be named 'Wipro Enterprises Ltd'.Prior to demerger, these companies together contributed about 10% of the revenues of Wipro Limited.

In 2014, Wipro signed a 10-year $1.2 billion contract with ATCO, a Canadian Energy and Utilities corporation based in Calgary, Alberta. This was the largest deal in Wipro's history. In October 2016, Wipro announced that it was buying Appirio, an Indianapolis-based cloud services company for $500 million. In 2017, the company expanded its operations in London.

In 2017, Wipro Limited won a five-year IT infrastructure and applications managed services engagement with Grameenphone (GP), a major telecom operator in Bangladesh and announced it would set up a new delivery centre there.

In 2018, the company began building software to help with the General Data Protection Regulation (GDPR) in Europe. In March 2018, Wipro said it would be buying a third of Denim Group. In April 2018, the company sold its stake in the airport IT services company JV.

In August 2018, Wipro paid US $75m to National Grid US as a settlement for a botched SAP implementation that a 2014 audit estimated could cost the company US $1 billion. Wipro had been hired as systems integrator in 2010, but errors in the rollout, intended to replace an Oracle system, caused serious losses and reputational damage.

To compete with Hindustan Unilever and Procter & Gamble, in May 2018 Wipro Consumer Care and Lighting announced it would increase Indian distribution of its acquired personal care brands Enchanteur and Yardley. Other recent acquisitions included Unza Holdings, LD Waxson, and Zhongshan  On May 3, 2018, it was announced that Wipro was opening manufacturing locations in Andhra Pradesh and Guangzhou. On May 4, 2018, it was reported that Wipro's stock value had been decreasing. The day earlier, it was reported that HCL would likely unseat Wipro that quarter as the third largest Indian IT company, after TCS and Infosys.
</p>
<p><b>2019</b></p><p>
In 2019, Wipro Consumer Care and the Ang-Hortaleza Corporation signed a share purchase agreement for the sale of 100% of the latter's stake in the personal care business of Splash Corporation, the companies announced on Monday, April 29.</p>
					 				<p><b>Subsidiaries</b></p>

<p><b>Western India Products Limited</b></p><p>
Wipro Limited is a provider of IT services, including Systems Integration, Consulting, Information Systems outsourcing, IT-enabled services, and R&D services.

Wipro entered into the technology business in 1981 and has over 160,000 employees and clients across 54 countries. IT revenues were at $7.1 billion for the year ended 31 March 2015, with a repeat business ratio of over 95%.</p>

<p><b>Wipro GE Medical Systems</b></p><p>
Wipro GE Medical Systems Limited is Wipro’s joint venture with GE Healthcare South Asia. It is engaged in the research and development of healthcare products. This partnership, which began in 1990, today includes gadgets and equipment for diagnostics, healthcare IT and services to help healthcare professionals combat cancer, heart disease, and other ailments. There is complete adherence to Six Sigma quality standards in all products. </p>
