					 				<h3>Company Information</h3>
					 				<p>Cognizant is an American multinational corporation that provides IT services, including digital, technology, consulting, and operations services. It is headquartered in Teaneck, New Jersey, United States. Cognizant is part of the NASDAQ-100 and trades under CTSH. It was founded as an in-house technology unit of Dun & Bradstreet in 1994,and started serving external clients in 1996.

After a series of corporate reorganizations there was an initial public offering in 1998. Following the Y2K and dot-com boom of the late 1990s, when companies sharpened their focus on hard business parameters such as revenues and profits, the company grew by delivering critical application development and maintenance services.

Cognizant had a period of fast growth during the 2000s, becoming a Fortune 500 company in 2011.

The company has been involved in a number of controversies in India, the United Kingdom, and the United States.

 </p>
					 				<p><b>History</b></p><p> Cognizant began as Dun & Bradstreet Satyam Software (DBSS),  established as Dun & Bradstreet's in-house technology unit focused on implementing large-scale IT projects for Dun & Bradstreet businesses. In 1996, the company started pursuing customers beyond Dun & Bradstreet.

In 1996, Dun & Bradstreet spun off several of its subsidiaries including Erisco, IMS International, Nielsen Media Research, Pilot Software, Strategic Technologies and DBSS, to form a new company called Cognizant Corporation. Three months later, in 1997, DBSS renamed itself to Cognizant Technology Solutions. In July 1997, Dun & Bradstreet bought Satyam's 24% stake in DBSS for $3.4 million. Headquarters were moved to the United States, and in March 1998, Kumar Mahadeva was named CEO. Operating as a division of the Cognizant Corporation, the company focused on Y2K-related projects and web development.

In 1998, the parent company, Cognizant Corporation, split into two companies: IMS Health and Nielsen Media Research. After this restructuring, Cognizant Technology Solutions became a public subsidiary of IMS Health. In June 1998, IMS Health partially spun off the company, conducting an initial public offering of the Cognizant stock. The company raised $34 million, less than what the IMS Health underwriters had hoped. They earmarked the money for debt payments and upgrading company offices.

Kumar Mahadeva decided to reduce the company's dependence on Y2K projects: by Q1 1999, 26% of company's revenues came from Y2K projects, compared with 49% in early 1998. Believing that the $16.6 billion enterprise resource planning software market was saturated, Kumar Mahadeva decided to refrain from large-scale ERP implementation projects. Instead, he focused on applications management, which accounted for 37% of Cognizant's revenue in Q1 1999.Cognizant's revenues in 2002 were $229 million, and the company had zero debt with $100 million in the bank. During the dotcom bust, the company grew by taking on the maintenance projects that larger IT services companies did not want.

</p>
					 				<p>In 2003, IMS Health sold its entire 56% stake in Cognizant, which instituted a poison pill provision to prevent hostile takeover attempts. Kumar Mahadeva resigned as the CEO in 2003, and was replaced by Lakshmi Narayanan. Gradually, the company's services portfolio expanded across the IT services landscape and into business process outsourcing (BPO) and business consulting. Lakshmi Narayanan was succeeded by Francisco D'Souza in 2006. Cognizant experienced a period of fast growth during the 2000s, as reflected by its appearance in Fortune magazine's "100 Fastest-Growing Companies" list for ten consecutive years from 2003 to 2012.

In September 2014, Cognizant struck its biggest deal, acquiring healthcare IT services provider TriZetto Corp for $2.7 billion. Cognizant Shares, rose nearly 3 percent in pre-market trading.

On 24 June 2015, the company signed a multimillion-dollar agreement with Escorts Group in India to help Escorts' businesses in digital transformation and modernizing its operations across all business segments.

On 30 June 2015, it partnered with Singapore-based supermarket retailer NTUC FairPrice to perform digital transformation in NTUC's business to improve personalized and consistent customer service across multiple channels.

In April 2018, Cognizant and a consortium of Indian life insurers announced their development of a blockchain solution aimed at increasing efficiency through facilitating cross-company data sharing. The platform which is built on Corda, a DLT platform developed by R3 was claimed to reduce dependency on third-party data intermediaries and aggregators for obtaining consumer profiles and policy details such as KYC due diligence, financial and medical underwriting, risk assessment, fraud detection and regulatory compliance. </p>
